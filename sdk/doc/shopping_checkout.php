<h1>Check out</h1>
<p>
The checkout is the part of the shopping experience where the actual shopping is done. The
checkout will be based on a plugin system where you can add as many steps as you like. The
point of the checkout is to create an order from the items placed in the cart.
</p>

<h2>Credit card integration</h2>
<p>
As a part of the checkout process you can choose to have one or more integrated payment
systems.
</p>

<h2>Shipping address / billing address</h2>
<p>
Each order must have specified one shipping address and one billing address, these addresses
<i>can</i> be the same.
</p>

<h2>Vouchers</h2>
<p>
Vouchers are gift certificates with a pre-payed ammount. These can be used to pay for part
of or the whole order.
</p>
