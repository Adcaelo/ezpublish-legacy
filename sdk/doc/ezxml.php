<p>
eZ xml is a library for handling XML documents.
</p>

<h2>XML</h2>
<p>
eZ xml supports handling of XML documents as defined in
<a href="http://www.w3.org/TR/2000/REC-xml-20001006">
Extensible Markup Language (XML) 1.0</a>
</p>

<h2>DOM level 2</h2>
<p>
eZ xml follows the <a href="http://www.w3.org/TR/2000/REC-DOM-Level-2-Core-20001113/core.html">DOM level 2</a>
standard from W3C.
</p>

<h2>Namespaces</h2>
<p>
Namespaces is supported as described by W3C in <a href="http://www.w3.org/TR/1999/REC-xml-names-19990114/">
Namespaces in XML</a>
</p>

<h2>XML Schemas</h2>
<p>
eZ xml supports the <a href="http://www.w3.org/TR/xmlschema-0/">XML Schema</a> standard
for validating XML documents.
</p>

