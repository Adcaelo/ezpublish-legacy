<h1>Wish list</h1>
<p>
The wish list is an alternative action which will store the reference to the
product in a list which is specific for the current user. The wish list is
defined pr user and the user must be logged in before an item can be added
to the users list.
</p>

<p>
The diagram below shows the different states of the wishlist. You can add
and remove products to your wish list. The wish list can also be sent to
a friend.
</p>

